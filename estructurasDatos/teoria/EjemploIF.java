public class EjemploIF{
		public static void main(String[] args){
			int numero = 77;
			
			if ( numero % 7 == 0){
				System.out.println( numero+ " es divisible por 7");
			}
			if ( numero % 11 == 0){
				System.out.println( numero+ " es divisible por 11");
			}
			System.out.println( "Fin del programa");
	}
}
